// palidrome.cpp (회문)
#include <algorithm> // equal()
#include <iostream>
#include <string>

using namespace std;

bool is_palidrome(const string& s) {
	return equal(s.begin(), s.end(), s.rbegin());
}

int main() {
	string s;
	while (cin >> s) {
		if (is_palidrome(s))
			cout << s << "은/는 회문입니다." << endl;
		else
			cout << s << "은/는 회문이 아닙니다." << endl;
	}
	return 0;
}